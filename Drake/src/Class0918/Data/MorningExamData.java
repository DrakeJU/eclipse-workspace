package Class0918.Data;

public class MorningExamData {
	//get set 만들기

	String name;
	int kor;
	int eng;
	int math;
	
	

	public void setName(String name) {
		this.name = name;
	}
	
	public  String getName() {
		return name;
	}
	
	public void setKor(int kor) {
		this.kor = kor;
	}
	
	public int getKor() {
		return kor;
	}
	public void setEng(int eng) {
		this.eng = eng;
	}
	public int getEng() {
		return eng;
	}
	public void setMath(int math) {
		this.math = math;
	}
	public int getMath() {
		return math;
	}
	
}